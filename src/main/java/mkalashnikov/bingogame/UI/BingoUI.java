    // Начать игру
    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        if (game == null || !game.getIsRunning()) {
            lblThumbnail.setVisible(false);
            level = 1;
            fieldSize = 4 + level;
            step = level + 1;
            stepsToUpDifficulty = level + 1;
            createButtons();
            panelGameField.setLayout(new GridLayout(fieldSize, fieldSize));

            game = new BingoGame();
            var modCells = game.start(1, true);
            renderCells(modCells);
            updateLabels();
        }
    }//GEN-LAST:event_btnStartActionPerformed

