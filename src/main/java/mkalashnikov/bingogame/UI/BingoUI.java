    // Увеличить сложность
    private void increaseDifficulty() {
        level++;
        fieldSize = 4 + level;
        step = level + 1;
        stepsToUpDifficulty = level + 1;
        createButtons();
        panelGameField.setLayout(new GridLayout(fieldSize, fieldSize));
        var modCells = game.start(level, false);
        renderCells(modCells);
        updateLabels();
    }

    // Обработчик нажатия на любую ячейку.
    private void bingoButtonActionPerformed(BingoButton btn) {
        if (!game.getIsRunning() || (previousPoint != null && (btn.rowIndex == previousPoint.x && btn.colIndex == previousPoint.y))) {
            return;
        }

        var modCells = game.markCell(new Point(btn.rowIndex, btn.colIndex), step == 0);
        var failReason = game.GetFailReason();
        if (!failReason.isEmpty()) {
            renderCells(game.GetFullField());
            JOptionPane.showMessageDialog(this, String.format("%s Ваш счет: %d", failReason, game.getScore()));
            panelGameField.setEnabled(false);
            game.stop();
            return;
        }

        renderCells(modCells);
        if (step > 0) {
            step--;
        } else {
            step = level + 1;
            stepsToUpDifficulty--;
            if (stepsToUpDifficulty <= 0) {
                JOptionPane.showMessageDialog(this, "Уровень пройден! Сложность увеличена!");
                increaseDifficulty();
                return;
            }
        }

        updateLabels();
        previousPoint = new Point(btn.rowIndex, btn.colIndex);
    }

