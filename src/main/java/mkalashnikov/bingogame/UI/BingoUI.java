    // Отрисовать кнопки-клетки
    private void renderCells(List<BingoPoint> modifiedCells) {
        var components = panelGameField.getComponents();
        for (var modCell : modifiedCells) {
            var cell = Arrays.stream(components)
                    .filter(p -> p instanceof BingoButton)
                    .map(bb -> (BingoButton) bb)
                    .filter(b -> b.rowIndex == modCell.x && b.colIndex == modCell.y)
                    .findFirst().get();
            if (modCell.cellState == CellState.Clean) {
                cell.setBackground(Color.WHITE);
            } else if (modCell.cellState == CellState.Marked) {
                cell.setBackground(Color.BLUE);
            } else if (modCell.cellState == CellState.Bingoed) {
                cell.setBackground(Color.GREEN);
            } else {
                cell.setBackground(Color.RED);
            }
        }

        panelGameField.revalidate();
    }

    // Обновить значения лейблов.
    private void updateLabels() {
        lblSize.setText(String.format("%sx%s", fieldSize, fieldSize));
        lblStepsToBingo.setText(String.valueOf(step));
        lblScore.setText(String.valueOf(game.getScore()));
    }

    // Заполнить панель кнопками-клетками.
    private void createButtons() {
        panelGameField.removeAll();
        for (int i = 0; i < fieldSize; i++) {
            for (int j = 0; j < fieldSize; j++) {
                var btn = new BingoButton(j, i);
                btn.setBackground(Color.WHITE);
                btn.addActionListener(l -> {
                    bingoButtonActionPerformed(btn);
                });
                panelGameField.add(btn);
            }
        }
    }

